public class ImprovedTeaching extends StdAbility
{
	@Override
	public String ID()
	{
		return "ImprovedTeaching";
	}

	private final static String	localizedName	= CMLib.lang().L("Improved Teaching");

	@Override
	public String name()
	{
		return localizedName;
	}

	@Override
	public String displayText()
	{
		return "";
	}

	@Override
	protected int canAffectCode()
	{
		return CAN_MOBS;
	}

	@Override
	protected int canTargetCode()
	{
		return 0;
	}

	@Override
	public int abstractQuality()
	{
		return Ability.QUALITY_BENEFICIAL_SELF;
	}

	@Override
	public boolean isAutoInvoked()
	{
		return true;
	}

	@Override
	public boolean canBeUninvoked()
	{
		return false;
	}

	@Override
	public int classificationCode()
	{
		return Ability.ACODE_COMMON_SKILL|Ability.DOMAIN_EDUCATIONLORE;
	}

	@Override
	public boolean okMessage(final Environmental myHost, final CMMsg msg)
	{
		if(!super.okMessage(myHost, msg))
			return false;
		if((msg.source()==affected)
		&&(msg.sourceMinor()==CMMsg.TYP_TEACH)
		&&(msg.tool() instanceof Ability))
		{
			final MOB mob=msg.source();
			if(super.proficiencyCheck(mob, 0, false))
			{
				super.helpProficiency(mob, 0);
				int bonus = 15;
				if(mob.fetchAbility(ID())!=null)
					bonus=(int)Math.round(CMath.mul(CMath.div(proficiency(),100.0), bonus));
				bonus += super.getXLEVELLevel(mob);
				msg.setValue(bonus);
			}
		}
		return true;
	}
}
